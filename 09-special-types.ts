// Special Type - null
let a: null;

// a = null
a = "Hello?"

let b: null | string

// b = null
// b = "Hello!"
b = 10.89

console.log(a)
console.log(b)

// Special Type - undefined
let c: undefined;

c = undefined
// c = null
// c = "Hello."

let d: undefined | string

d = undefined
d = "Hello..."
// d = null
// d = 10

console.log(c)
console.log(d)